//Tableau de liste pour cliquer sur chaque étudiant pour afficher ses données
    private void jTableListeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableListeMouseClicked
        int ligne=jTableListe.getSelectedRow();
        String id= jTableListe.getModel().getValueAt(ligne,0).toString();
        String sql="Select * from etudiant where id ='"+id+"'";
        Image image = null ;
        
        try{
            PreparedStatement ps =con.prepareStatement(sql);
             ResultSet rs;
             rs =ps.executeQuery();
            if(rs.next()){
                    jTextFieldNom.setText(rs.getString("nom"));
                    jTextFieldPrenom.setText(rs.getString("prenom"));
                    ((JTextField)bdate.getDateEditor().getUiComponent()).setText(rs.getString("date_naiss"));
                    jTextFieldFiliere.setText(rs.getString("filiere"));
                    jTextFieldFaculte.setText(rs.getString("faculte"));
                    jTextFieldAnnee_Etude.setText(rs.getString("annee_etude"));
                    jTextFieldMatricule.setText(rs.getString("matricule"));
                    InputStream binaryStream1 = rs.getBinaryStream("photo");
                try {
                    image = ImageIO.read(binaryStream1);
                } catch (IOException ex) {
                    java.util.logging.Logger.getLogger(Etudiant.class.getName()).log(Level.SEVERE, null, ex);
                }   
            
                    JLabelphoto.setIcon(new ImageIcon(image));
                    
                   
                   
                    
                    
                    jLabelNomCarte.setText(rs.getString("nom"));
                    jLabelPrenomCarte.setText(rs.getString("prenom"));
                    jLabelDate_naissCarte.setText(rs.getString("date_naiss"));
                    jLabelFiliereCarte.setText(rs.getString("filiere"));
                    jLabelFaculteCarte.setText(rs.getString("faculte"));
                    jLabelAnnee_etudeCarte.setText(rs.getString("annee_etude"));
                    jLabelMatriculeCarte.setText(rs.getString("matricule"));
                InputStream binaryStream = rs.getBinaryStream("photo");
                try {
                    image = ImageIO.read(binaryStream);
                } catch (IOException ex) {
                    java.util.logging.Logger.getLogger(Etudiant.class.getName()).log(Level.SEVERE, null, ex);
                }

                    
                    
            }
            
            jLabelPhotoCarte.setIcon(new ImageIcon(image));
        }catch (SQLException e1){
            e1.printStackTrace();
        } 
        
    }//GEN-LAST:event_jTableListeMouseClicked

